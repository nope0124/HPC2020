#pragma once
#include "Scroll.hpp"

//------------------------------------------------------------------------------
namespace hpc {

//------------------------------------------------------------------------------
Scroll::Scroll()
: mPosition()
, mIsGotten(false)
{
}

//------------------------------------------------------------------------------
Scroll::Scroll(const Vector2& aPos)
: mPosition(aPos)
, mIsGotten(false)
{
}

//------------------------------------------------------------------------------
const Vector2& Scroll::pos() const
{
    return mPosition;
}

//------------------------------------------------------------------------------
bool Scroll::isGotten() const
{
    return mIsGotten;
}

//------------------------------------------------------------------------------
void Scroll::setIsGotten(bool aIsGotten)
{
    mIsGotten = aIsGotten;
}

} // namespace
